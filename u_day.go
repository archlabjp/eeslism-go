package main

func FNNday(Mo int, Nd int) int {
	var Mo2, Mo3 int
	if Mo < 3 {
		Mo2 = 1
		Mo3 = 0
	} else {
		Mo2 = 0
		Mo3 = 1
	}
	return int((153*(Mo-1)+2*(Mo2)-9*(Mo3))/5 + Nd)
}
