/*
common.go (Common Constants for Building Energy Simulation)

このファイルは、建物のエネルギーシミュレーション全体で共通して使用される定数を定義します。
これらの定数は、熱計算の基礎となる物理定数、エラー処理のためのコード、
および特定の閾値などを統一的に管理するために用いられます。

建築環境工学的な観点:
- **物理定数**: 熱計算や流体計算において、
  - `NOP`: 演算なし、または無効な状態を示すコード。
  - `FNOP`: 関数が有効な値を返さない場合に使用されるダミー値。
  - `TEMPLIMIT`: 絶対零度に近い温度など、物理的に意味のない温度の閾値。
    これらの定数は、シミュレーションの正確性と安定性を確保するために重要です。
  - **エラー処理**: `ERRFMT`, `ERRFMTA`は、
    エラーメッセージのフォーマットを定義するための定数です。
    これにより、シミュレーション中に発生した問題を統一的な形式で報告し、
    デバッグや問題解決を効率化できます。
  - **統一的な基準**: これらの共通定数を定義することで、
    シミュレーションモデル全体で統一された基準が適用され、
    異なるモジュール間でのデータの整合性が保たれます。
    これは、大規模なシミュレーションモデルの構築と保守において非常に重要です。

このファイルは、建物のエネルギーシミュレーションの基盤を形成し、
計算の正確性、安定性、および保守性を向上させるための重要な役割を果たします。
*/
package eeslism

const (
	NOP       = -1
	FNOP      = FNAN
	TEMPLIMIT = -273.16
	ERRFMT    = "xxxxx %s xxxxx : "
	ERRFMTA   = "xxx %s xxx %s\n"
)
